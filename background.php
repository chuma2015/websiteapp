<p>Before establishment of COASCO, the cooperative societies were being audited by the then Audit and Supervision Fund (ASF),
				a fund that was under the Prime Minister’s Office since 1965. </p>
			<p>The Co-operative Audit and Supervision Corporation was  established   in 1982 by an Act of Parliament No. 15 of 1982 to be the sole public corporation
				that provided Audit, Supervision  and  Consultancy services to the cooperatives in Tanzania Mainland. During its establishment, COASCO took over all 
				the assets and liabilities that were owned by ASF including employees.</p>
			<p>The Act was amended in April 2005 and assented by the President of The United Republic of Tanzania on 6th June, 2005 to empower the corporation to provide 
			Audit, Supervision, Research, Consultancy and Other related services to Cooperatives as well as to non-Cooperative entities in Tanzania</p>
			
