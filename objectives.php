			<p>1.0.	General Objectives of the Corporation<br>
The overall strategic objective of the Corporation is to transform the corporation into a professionally competent entity that provides auditing and consultancy services commercially to the cooperative movement and related entities in the associative economy.
<br>
1.2.	Specific Objectives
It   is recommended that the Corporation should always direct itself on the following broad strategic goals:<br>
(i)	To transform the Corporation into a professionally competent Audit, Research and Consulting Organization that provides audit, supervision, research and consultancy services commercially to the clients of the Corporation in Tanzania.<br>
(ii)	To address Audit function in order to make more positive impact on the Co-operative Movement in Tanzania.<br>
(iii)	To enhance the quality of the Corporation’s Supervisory role by empowering the members to take a leading role in the management of their own societies’ affairs.<br>
(iv)	To enhance sustainability through improved internal revenue generation capacity in a cost effective way.<br>
(v)	To improve employees’ social welfare, physical infrastructure and participation in decision making.

