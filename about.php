<p>
				The Co-operative Audit and Supervision Corporation (COASCO) were legally established   in 1982 by on Act of Parliament No. 15 of 1982. 
				Its primary function was to provide audit, supervision and other consultancy services to Co-operatives in Tanzania.  The Act was amended by the
				Parliament in April 2005 and assented by the President of The United Republic of Tanzania on 6th June, 2005 to empower the corporation to extent its audit and 
				Consultancy services as well to non-Cooperative entities.
